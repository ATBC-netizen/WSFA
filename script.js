// Ensures the "start" function is only called for when the page has been fully loaded, if this were not the case, errors may be present as the script may attempt to access elements which have not yet been rendered.

window.addEventListener('load', start)

var vehicleSearchForm = document.forms.vehicleSearchForm // Retrieves the Vehicle Search Form from HTML.
var vehicles = getVehicles(); // Fetches the vehicle information from the JSON file and stores it as a promise.
var container = document.getElementById('display-results') // The container in which the results are displayed on the webpage.
var modal = document.getElementById('modal') // Retrieves the modal from HTML.
var modalBody = document.getElementById('modalBody') // The container in which more detailed information about the vehicle is displayed within the modal element.

// Displays the vehicle results which have been filtered by other functions by creating a card (an "article" element) for each vehicle, which is then populated with the relevant information and appended to the container.
// If no vehicles are found matching the search criteria, a message is displayed notifying the user that none were found matching their criteria.
// Calls for the "openModal" function if a card is clicked on, where a modal is then opened with extra information about the vehicle.
// The "openModal" function can also be called by pressing the "Enter" key when a card is selected, for accessibility purposes.

function displayResults(vehicles) {
    container.innerHTML = ""; // Clears the container of any previously displayed results.

    if (vehicles.length === 0) {
        container.innerText = "No vehicles found matching your criteria.";
        return;
    } 

    vehicles.forEach(function (vehicle) {
        var card = document.createElement('article');
        card.classList.add('vehicle-card');

        card.innerHTML = `
            <h2>${vehicle.make} ${vehicle.model}</h2>
            <p><strong>Year:</strong> ${vehicle.year}</p>
            <p><strong>Transmission:</strong> ${vehicle.transmission}</p>
            <p><strong>Fuel Type:</strong> ${vehicle.fuelType}</p>
            <p><strong>Price:</strong> £${vehicle.price}</p>
            <p><strong>Engine Size:</strong> ${vehicle.engineSize}</p>
        `;

        card.addEventListener('click', function() {
            openModal(vehicle);
        });

        card.addEventListener('keypress', function (e) {
            if (e.key === 'Enter') {
                openModal(vehicle);
            }
        });

        container.appendChild(card); // Appends the 'container' element, which displays the results in the 'display-results' section of the webpage.
    });
}

// Opens a modal with extra information about the vehicle when a card is selected.
// The modal is generated by setting the innerHTML of the modal's body and the "hidden" class is removed from the modal to make it visible.

function openModal(vehicle) {
    modalBody.innerHTML = `
        <h2>${vehicle.make} ${vehicle.model}</h2>
        <p>Year: ${vehicle.year}</p>
        <p>Transmission: ${vehicle.transmission}</p>
        <p>Fuel Type: ${vehicle.fuelType}</p>
        <p>Price: £${vehicle.price}</p>
        <p>Mileage: ${vehicle.mileage} miles</p>
        <p>MPG: ${vehicle.mpg}</p>
        <p>Tax: £${vehicle.tax}/year</p>
        <p>Engine Size: ${vehicle.engineSize}L</p>
    `;
    modal.classList.remove('hidden');
}

// Fetches the vehicle data from the JSON file and returns a promise which resolves to the parsed JSON data. The promise is stored in the "vehicles" variable, which is globally accessible.

function getVehicles() {
    return fetch("vehicleStorage.json")
    .then(function (data) {
        return data.json();
    })
}
