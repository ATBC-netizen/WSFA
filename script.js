// Creates and populates the dropdowns by mapping the vehicle information to an array of the relevant field, converting it to a "Set" to remove duplicates, and then creating an "option" element for each unique value which is then appended to the relevant dropdown container.

function createDropdown(vehicleData, fieldID) {
    vehicleData.then(function (allVehicles) {
        var data = allVehicles.map(function (item) {
            return item[fieldID];
        });
        data = new Set(data);

        var dropdownContainer = document.getElementById(fieldID + 'Filter')
        data.forEach(function (item) {
            var option = document.createElement('option');
            option.value = item;
            option.innerText = item;
            dropdownContainer.appendChild(option);
        });
    })
}

function handleVehicleSearch(e) {
    e.preventDefault(); // Prevents the default action of submission, which in this instance would refresh the page.

    vehicles.then(function (allVehicles) {
        searchAndDisplayResults(allVehicles);
        console.log(container)
    });
}

// Filters the information based on the search criteria by checking if the search string is set to "All", and if it is not, the function then checks if the property being filtered is a boolean or a string, where it is then compared to the search string, with .toLowerCase() only being applied to string values to avoid errors.

function filterBy(data, searchString, prop) {
    return data.filter(function (item) {
        if (searchString === "All") return true;

        if (typeof(item[prop]) === "boolean") {
            return item[prop] === searchString;
        }

        if (typeof(item[prop]) === "string") {
            return item[prop].toLowerCase() === searchString.toLowerCase();
        }
        return false;
    });
}

// Filters the information based on a range (user-input), used to avoid hard-coding the function and allows the function to be used again in the future if necessary.

function rangeFilter(data, misc) {
    var min = Number(document.getElementById(misc.minID).value) || 0
    var max = Number(document.getElementById(misc.maxID).value) || Infinity

    return data.filter(function (item) {
        return item[misc.prop] >= min && item[misc.prop] <= max
    })
}

// Filters the information based on the searchString, used to avoid hard-coding the function and allows the function to be used again in the future if necessary.

function miscFilter(data, misc) {
    var searchString = document.getElementById(misc.id).value

    return filterBy(data, searchString, misc.prop)
}

function searchAndDisplayResults(vhcs) {

    // Variables for the miscFilter function. An array of objects is used to avoid hard-coding the function and allows for expansion in the future if necessary.
    // If a new range range needs to be added, all that is required is the addition of a new object within the array below.

    var miscFilters = [
        { prop: "make", id: "makeFilter" },
        { prop: "model", id: "modelFilter" },
        { prop: "transmission", id: "transmissionFilter" },
        { prop: "fuelType", id: "fuelTypeFilter" }
    ]

    // Variables for the rangeFilter function. An array of objects is used to avoid hard-coding the function and allows for expansion in the future if necessary.
    // If a new range range needs to be added, all that is required is the addition of a new object within the array below.

    var rangeFilters = [
        { prop: "year", minID: "minYear", maxID: "maxYear" },
        { prop: "price", minID: "minPrice", maxID: "maxPrice" },
        { prop: "mileage", minID: "minMileage", maxID: "maxMileage" },
        { prop: "mpg", minID: "minMPG", maxID: "maxMPG" },
        { prop: "tax", minID: "minTax", maxID: "maxTax" },
        { prop: "engineSize", minID: "minEngineSize", maxID: "maxEngineSize" }
    ]

    var vehiclesFound = vhcs // Creates the variable "vehiclesFound" and populates it with the contents of "vhcs", the list of all the vehicles.

    miscFilters.forEach(function (misc) {
        vehiclesFound = miscFilter(vehiclesFound, misc)
    })

    rangeFilters.forEach(function (misc) {
        vehiclesFound = rangeFilter(vehiclesFound, misc)
    })

    displayResults(vehiclesFound)
}

// Calls forth the createDropdown function for each of the relevant fields which populates the dropdowns with the relevant information, and adds an event listener to the form which calls for the handleVehicleSearch function when the "submit" event (in the form of a "submit button" is clicked) occurs.

function start() {
    createDropdown(vehicles, "make")
    createDropdown(vehicles, "model")
    createDropdown(vehicles, "transmission")
    createDropdown(vehicles, "fuelType")
    vehicleSearchForm.addEventListener('submit', handleVehicleSearch)

    // Closes (or hides) the modal when the user clicks the "x" button by adding the "hidden" class to the modal.

    document.getElementById('closeModal').addEventListener('click', function() {
        modal.classList.add('hidden')
    })

    // Closes (or hides) the modal when the user clicks outside of the modal's content area by adding the "hidden" class to the modal.

    window.addEventListener('click', function(event) {
        if (event.target === modal) {
            modal.classList.add('hidden')
        }
    })
}